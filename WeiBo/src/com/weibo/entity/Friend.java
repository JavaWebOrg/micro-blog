package com.weibo.entity;

public class Friend {
	private int hid;
	private int xuid;
	private int yuid;
	private String xunane;
	private String yuname;
	private int uid;
	public int getHid() {
		return hid;
	}
	public void setHid(int hid) {
		this.hid = hid;
	}
	public int getXuid() {
		return xuid;
	}
	public void setXuid(int xuid) {
		this.xuid = xuid;
	}
	public int getYuid() {
		return yuid;
	}
	public void setYuid(int yuid) {
		this.yuid = yuid;
	}
	public String getXunane() {
		return xunane;
	}
	public void setXunane(String xunane) {
		this.xunane = xunane;
	}
	public String getYuname() {
		return yuname;
	}
	public void setYuname(String yuname) {
		this.yuname = yuname;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	
	
}
